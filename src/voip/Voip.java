    private void sendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendActionPerformed
        // TODO add your handling code here:
        
        try{
            Contact selectedCont = (Contact)jList1.getSelectedValue();
            String msg = jTextArea1.getText();
            
            if(msg.length() > 0){
                if(msg.length() > 40){
                    StringBuilder str = new StringBuilder(msg);

                    for(int i=40; i<str.length(); i+=40){
                        str.insert(i, "\n");
                    }
                    msg = str.toString();
                }
                msg += "<br/>";

                InetAddress IP = selectedCont.getIP();

                try{
                    sendingSocket = new DatagramSocket();
                }catch (SocketException e){
                    JOptionPane.showMessageDialog(null, "Could not open UDP socket to send from",
                    "Error", JOptionPane.ERROR_MESSAGE);
                }

                //Convert it to an array of bytes
                byte[] buffer = msg.getBytes();

                //Make a DatagramPacket from it, with client address and port number
                DatagramPacket packet = new DatagramPacket(buffer, buffer.length, IP, PORT);

                //Send it
                sendingSocket.send(packet);

                selectedCont.addMessage("<b>" + System.getProperty("user.name") + ":</b> " + msg);
                jTextPane1.setText(selectedCont.getChat());
                jTextArea1.setText("");
            }else{
                JOptionPane.showMessageDialog(null, "You cannot send empty messages",
                    "Input Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        catch(NullPointerException ex){
            JOptionPane.showMessageDialog(null, "No contact selected",
                    "Error", JOptionPane.ERROR_MESSAGE);
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Cannot send",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
            
    }//GEN-LAST:event_sendActionPerformed

