    private void callButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_callButtonActionPerformed
        // TODO add your handling code here:
        Contact selectedCont = (Contact)jList1.getSelectedValue();
        
        if(callCount == 0){
            if(selectedCont != null){
                transmitVoice = new Transmitter(selectedCont.getIP(),16454);
                (new Thread(transmitVoice)).start();
                callCount++;
            }else{
                JOptionPane.showMessageDialog(null, "No contact selected",
                        "Error", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Only one call per time allowed",
                        "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_callButtonActionPerformed

