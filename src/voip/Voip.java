    public static void addMessage(String msg, InetAddress addr) throws InterruptedException{
        for(int i=0; i<jList1.getModel().getSize(); i++){
            Contact tempC = (Contact)jList1.getModel().getElementAt(i);
            if(tempC.getIP().equals(addr)){
                tempC.addMessage("<b>" + tempC.getName() + ":</b> " +msg);
            }
            
            Contact currentC = (Contact)jList1.getSelectedValue();
            sleep(300);
            jTextPane1.setText(currentC.getChat());
        }
    }
    
